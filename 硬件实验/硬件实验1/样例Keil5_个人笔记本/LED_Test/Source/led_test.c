#include "2410lib.h"

/*------------------------------------------------------------------------------------------*/
/*                                     constant define	                                    */
/*------------------------------------------------------------------------------------------*/
#define rLEDADDR  (*(volatile unsigned char*)0x21180000) 			// LED Address

/*********************************************************************************************
* name:		led_on
* func:		turn on the leds one by one
* para:		none
* ret:		none
* modify:
* comment:		
*********************************************************************************************/
void led_on(void)
{
	int i;

	rLEDADDR = 0xFE;
	for(i = 0; i < 1000000; i++);
	rLEDADDR = 0xFC;  
	for(i = 0; i < 1000000; i++);	
	rLEDADDR = 0xF8;   
	for(i = 0; i < 1000000; i++);	
	rLEDADDR = 0xF0;  
	for(i = 0; i < 1000000; i++);

}


/*********************************************************************************************
* name:		led_test
* func:		i/o control test(led)
* para:		none
* ret:		none
* modify:
* comment:		
*********************************************************************************************/
void led_test(void)
{

	uart_printf("\n Expand I/O (Diode Led) Test Example\n");
	uart_printf(" Please Look At The LEDS \n");
	led_on();
	delay(2000);
	uart_printf(" End.\n");
}
